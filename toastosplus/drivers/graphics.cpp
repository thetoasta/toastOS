void flush() {
    if (!is_ready) return;
    /* Copy backbuffer → LFB line by line (pitch may differ) */
    uint32_t line_pixels = scr_w;
    for (uint32_t y = 0; y < scr_h; y++) {
        uint32_t *dst = reinterpret_cast<uint32_t*>(reinterpret_cast<uint8_t*>(lfb) + y * scr_pitch);
        uint32_t *src = backbuf + y * scr_w;
        fast_copy32(dst, src, line_pixels);
    }
}

/* Primitives */
void clear(uint32_t color) {
    if (!is_ready) return;
    fast_set32(backbuf, color, scr_w * scr_h);
}

void pixel(int x, int y, uint32_t color) {
    if (!is_ready) return;
    if (static_cast<unsigned>(x) >= scr_w || static_cast<unsigned>(y) >= scr_h) return;
    backbuf[y * scr_w + x] = color;
}

void rect(int x, int y, int w, int h, uint32_t color) {
    if (!is_ready) return;
    for (int row = y; row < y + h; row++) {
        if (static_cast<unsigned>(row) >= scr_h) break;
        if (row < 0) continue;
        uint32_t *line = backbuf + row * scr_w;
        int x0 = x < 0 ? 0 : x;
        int x1 = (x + w) > static_cast<int>(scr_w) ? static_cast<int>(scr_w) : (x + w);
        for (int col = x0; col < x1; col++)
            line[col] = color;
    }
}

void rect_alpha(int x, int y, int w, int h, uint32_t color) {
    if (!is_ready) return;
    for (int row = y; row < y + h; row++) {
        if (static_cast<unsigned>(row) >= scr_h) break;
        if (row < 0) continue;
        uint32_t *line = backbuf + row * scr_w;
        int x0 = x < 0 ? 0 : x;
        int x1 = (x + w) > static_cast<int>(scr_w) ? static_cast<int>(scr_w) : (x + w);
        for (int col = x0; col < x1; col++)
            line[col] = blend(color, line[col]);
    }
}

void outline(int x, int y, int w, int h, uint32_t color, int thickness) {
    if (!is_ready) return;
    /* Top */
    rect(x, y, w, thickness, color);
    /* Bottom */
    rect(x, y + h - thickness, w, thickness, color);
    /* Left */
    rect(x, y + thickness, thickness, h - 2 * thickness, color);
    /* Right */
    rect(x + w - thickness, y + thickness, thickness, h - 2 * thickness, color);
}

/* Text rendering (8×16 bitmap font) */
void draw_char(int x, int y, char c, uint32_t fg, uint32_t bg) {
    if (!is_ready) return;
    unsigned char ch = static_cast<unsigned char>(c);
    if (ch < 32 || ch > 126) ch = 32;
    const uint8_t *glyph = font_8x16[ch - 32];

    for (int row = 0; row < FONT_H; row++) {
        int sy = y + row;
        if (static_cast<unsigned>(sy) >= scr_h) break;
        if (sy < 0) continue;
        uint8_t bits = glyph[row];
        uint32_t *line = backbuf + sy * scr_w;
        for (int col = 0; col < FONT_W; col++) {
            int sx = x + col;
            if (static_cast<unsigned>(sx) >= scr_w) break;
            if (sx < 0) continue;
            line[sx] = (bits & (0x80 >> col)) ? fg : bg;
        }
    }
}

void text(int x, int y, const char *str, uint32_t fg, uint32_t bg) {
    while (*str) {
        if (*str == '\n') {
            y += FONT_H;
            x = 0;
        } else {
            draw_char(x, y, *str, fg, bg);
            x += FONT_W;
        }
        str++;
    }
}

void text(int x, int y, const char *str, uint32_t fg) {
    if (!is_ready) return;
    while (*str) {
        if (*str == '\n') {
            y += FONT_H;
            x = 0;
        } else {
            unsigned char ch = static_cast<unsigned char>(*str);
            if (ch < 32 || ch > 126) ch = 32;
            const uint8_t *glyph = font_8x16[ch - 32];
            for (int row = 0; row < FONT_H; row++) {
                int sy = y + row;
                if (static_cast<unsigned>(sy) >= scr_h) break;
                if (sy < 0) continue;
                uint8_t bits = glyph[row];
                uint32_t *line = backbuf + sy * scr_w;
                for (int col = 0; col < FONT_W; col++) {
                    int sx = x + col;
                    if (static_cast<unsigned>(sx) >= scr_w) break;
                    if (sx < 0) continue;
                    if (bits & (0x80 >> col))
                        line[sx] = fg;
                }
            }
            x += FONT_W;
        }
        str++;
    }
}

/* Blit */
void blit(int dx, int dy, int w, int h, const uint32_t *src) {
    if (!is_ready || !src) return;
    for (int row = 0; row < h; row++) {
        int sy = dy + row;
        if (static_cast<unsigned>(sy) >= scr_h) break;
        if (sy < 0) { src += w; continue; }
        uint32_t *dst = backbuf + sy * scr_w;
        int x0 = dx < 0 ? -dx : 0;
        int x1 = (dx + w) > static_cast<int>(scr_w) ? static_cast<int>(scr_w) - dx : w;
        for (int col = x0; col < x1; col++)
            dst[dx + col] = src[col];
        src += w;
    }
}

