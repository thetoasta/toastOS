/* Fast word-copy (uint32 aligned) */
void fast_copy32(uint32_t *dst, const uint32_t *src, uint32_t count) {
    while (count >= 8) {
        dst[0]=src[0]; dst[1]=src[1]; dst[2]=src[2]; dst[3]=src[3];
        dst[4]=src[4]; dst[5]=src[5]; dst[6]=src[6]; dst[7]=src[7];
        dst += 8; src += 8; count -= 8;
    }
    while (count--) *dst++ = *src++;
}

void fast_set32(uint32_t *dst, uint32_t val, uint32_t count) {
    while (count >= 8) {
        dst[0]=val; dst[1]=val; dst[2]=val; dst[3]=val;
        dst[4]=val; dst[5]=val; dst[6]=val; dst[7]=val;
        dst += 8; count -= 8;
    }
    while (count--) *dst++ = val;
}

