/* Alpha blending */
uint32_t blend(uint32_t fg, uint32_t bg) {
    uint32_t a = (fg >> 24) & 0xFF;
    if (a == 0xFF) return fg & 0x00FFFFFF;
    if (a == 0x00) return bg & 0x00FFFFFF;
    uint32_t inv = 255 - a;
    uint32_t r = ((((fg >> 16) & 0xFF) * a) + (((bg >> 16) & 0xFF) * inv)) / 255;
    uint32_t g = ((((fg >>  8) & 0xFF) * a) + (((bg >>  8) & 0xFF) * inv)) / 255;
    uint32_t b = ((( fg        & 0xFF) * a) + ((bg         & 0xFF) * inv)) / 255;
    return (r << 16) | (g << 8) | b;
}

/* Init / query */
void init(uint32_t *framebuffer, uint32_t width, uint32_t height, uint32_t pitch) {
    lfb       = framebuffer;
    scr_w     = width;
    scr_h     = height;
    scr_pitch = pitch;

    /* Allocate backbuffer in system RAM */
    backbuf = static_cast<uint32_t*>(mem::alloc(width * height * 4));
    if (!backbuf) return;

    /* Clear both buffers */
    fast_set32(backbuf, 0, width * height);
    is_ready = true;
}

bool ready() { return is_ready; }

uint32_t* backbuffer() { return backbuf; }

